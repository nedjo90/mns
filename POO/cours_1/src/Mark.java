public class Mark {
    private int note;
    private int rate;

    public Mark(int note, int rate) {
        this.note = note;
        this.rate = rate;
    }

    public int getNote() {
        return note;
    }

    public void setNote(int note) {
        this.note = note;
    }

    public int getRate() {
        return rate;
    }

    public void setRate(int rate) {
        this.rate = rate;
    }
}
